class Command(BaseCommand):
    help = 'Idempotently seed activity types, tag categories, tags, weights and achievements.'

    def handle(self, *args, **options):
        from habits.models import (
            Achievement,
            ActivityType,
            Tag,
            TagCategory,
            TagCategoryWeight,
        )

        with transaction.atomic():
            for name in CATEGORIES:
                TagCategory.objects.update_or_create(
                    name=name,
                    defaults={'slug': slugify(name) or name},
                )
            for name, emoji in ACTIVITY_TYPES:
                ActivityType.objects.update_or_create(
                    name=name, defaults={'emoji': emoji}
                )
            for name, atype, hint in TAGS:
                at = ActivityType.objects.get(name=atype)
                Tag.objects.update_or_create(
                    name=name,
                    defaults={
                        'slug': slugify(name) or name,
                        'activity_type': at,
                        'hint': hint,
                    },
                )
            for tag_name, weights in WEIGHTS.items():
                tag = Tag.objects.get(name=tag_name)
                for cat_name, weight in weights.items():
                    cat = TagCategory.objects.get(name=cat_name)
                    TagCategoryWeight.objects.update_or_create(
                        tag=tag, category=cat, defaults={'weight': weight}
                    )
            for code, title, desc, ctype, cval, xp, icon in ACHIEVEMENTS:
                Achievement.objects.update_or_create(
                    code=code,
                    defaults={
                        'title': title,
                        'description': desc,
                        'condition_type': ctype,
                        'condition_value': cval,
                        'xp_reward': xp,
                        'icon': icon,
                    },
                )
        self.stdout.write(self.style.SUCCESS(
            f'Seeded {TagCategory.objects.count()} categories, '
            f'{Tag.objects.count()} tags, '
            f'{Achievement.objects.count()} achievements.'
        ))
